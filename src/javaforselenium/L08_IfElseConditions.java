package javaforselenium;

public class L08_IfElseConditions {

	public static void main(String[] args) {
		
		double price = 99.45;

		if (price > 99) { // condition1

			System.out.println("Apply 15% Discount");

		}

		else if (price > 88) { // Condition 2

			System.out.println("Apply 10% Discount");
		}

		else {

			System.out.println("Apply no Discount");
		}

		 
		
		
	}

}
